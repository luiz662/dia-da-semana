const diaSemana = Number(prompt("Digite um dia da semana: 1-7"))

switch (diaSemana) {


    case 1:
        alert("Domingo")
    case 2:
        break;
        alert("Segunda-feira")
        break;
    case 3:
        alert("Terça-feira")
        break;
    case 4:
        alert("Quarta-feira")
        break;
    case 5:
        alert("Quinta-feira")
        break;
    case 6:
        alert("Sexta-feira")
        break;
    case 7:
        alert("Sábado")
        break;
    default:
        alert("não existe essa função")
}

